										<p>Terlahir di Jakarta, 14 April 1965, Sundari Sukotjo menjadi salah seorang penyanyi keroncong wanita. Pendidikannya adalah D-3 IKIP pada tahun 1987 dan sarjana musik dari Universitas Negeri Jakarta pada tahun 2002. Ia juga pernah mengajar sebagai guru kesenian di SMA 38 Jakarta.</p>
